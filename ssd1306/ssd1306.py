class Oled:
    def __init__(self,scl,sda):
        self.i2c = I2C(I2C.I2C0, mode=I2C.MODE_MASTER, freq=400000, scl=scl, sda=sda, addr_size=7)
        self.oled_init()
        self.oled_clear()
    def oled_init(self):
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xAE, mem_size=8)#关显示
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x20, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x10, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xb0, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xc8, mem_size=8)#微调对比度
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x00, mem_size=8)#微调对比度的值，可设置范围0x00～0xff
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x10, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x40, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x81, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xff, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xa1, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xa6, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xa8, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x3F, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xa4, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xd3, mem_size=8)#显示偏移
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x00, mem_size=8)#不偏移
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xd5, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xf0, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xd9, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x22, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xda, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x12, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xdb, mem_size=8)#Set VCOMH Deselect Level
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x20, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x8d, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0x14, mem_size=8)
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0xaf, mem_size=8)#开显示
    def oled_on(self):
        self.i2c.writeto_mem(SSD1306_ADDR, 0x00, 0X8D, mem_size=8)
